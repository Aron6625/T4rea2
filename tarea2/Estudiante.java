public class Estudiante {
    private String nomnbre;
    private int nota;
    
    public Estudiante() {
        nomnbre = "";
        nota = 0;
    }

    public String getNomnbre() {
        return nomnbre;
    }

    public void setNomnbre(String nomnbre) {
        this.nomnbre = nomnbre;
    }

    public int getNota() {
        return nota;
    }

    public void setNota(int nota) {
        this.nota = nota;
    }

}
